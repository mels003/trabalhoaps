function f1(a, b) {
  return a + b;
}

function f2(a, b) {
  return a - b;
}

function f3(a, b) {
  return a * b;
}

function f4(a, b) {
  return a / b;
}

const x = 10;
const y = 5;

console.log(`Soma: ${f1(x, y)}`);
console.log(`Subtração: ${f2(x, y)}`);
console.log(`Multiplicação: ${f3(x, y)}`);
console.log(`Divisão: ${f4(x, y)}`);